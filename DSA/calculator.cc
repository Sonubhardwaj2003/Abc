#include <iostream>
using namespace std;
int main()
{
    int a, b;
    cout << "enter the value of a = ";
    cin >> a;
    cout << "enter the value of b = ";
    cin >> b;

    cout << "sum = " << (a + b) << endl;
    cout << "difference = " << (a - b) << endl;
    cout << "multiply = " << (a * b) << endl;
    cout << "divide = " << (a / b) << endl;
    
    return 0;
}
